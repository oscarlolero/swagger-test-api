// Generate availability for the year
export function generateAvailabilityForYear(year) {
  const availability = {};
  const daysInMonth = [31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31];

  // Check for leap year
  if ((year % 4 === 0 && year % 100 !== 0) || year % 400 === 0) {
    daysInMonth[1] = 29;
  }

  for (let month = 0; month < 12; month++) {
    for (let day = 1; day <= daysInMonth[month]; day++) {
      const date = `${year}-${String(month + 1).padStart(2, '0')}-${String(day).padStart(2, '0')}`;
      availability[date] = true;
    }
  }

  // Introduce random unavailability ranges
  const totalDays = Object.keys(availability).length;
  const randomRanges = Math.floor(totalDays / 15); // Adjust this number to control frequency

  for (let i = 0; i < randomRanges; i++) {
    const startDay = Math.floor(Math.random() * totalDays);
    const rangeLength = Math.floor(Math.random() * 2) + 2; // 2-3 days

    let count = 0;
    for (const date in availability) {
      if (count >= startDay && count < startDay + rangeLength) {
        availability[date] = false;
      }
      count++;
    }
  }

  return availability;
}


